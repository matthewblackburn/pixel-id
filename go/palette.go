package pixelid

// Color represents an RGB color.
type Color struct {
	R, G, B uint8
}

// Hex returns the color as a "#RRGGBB" string.
func (c Color) Hex() string {
	const hex = "0123456789ABCDEF"
	return string([]byte{
		'#',
		hex[c.R>>4], hex[c.R&0x0F],
		hex[c.G>>4], hex[c.G&0x0F],
		hex[c.B>>4], hex[c.B&0x0F],
	})
}

